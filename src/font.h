static inline const uint8_t* fontGlyph(char c) {
    if (c >= 'a' && c <= 'z') c = (char)(c - 'a' + 'A');
    if (c < 32 || c > 95) c = '?';
    return FONT_GLYPHS[c - 32];
}
static inline int fontTextWidth(const char* s, int scale = 1) {
    int n = 0;
    while (*s++) n++;
    return n > 0 ? (n * 6 - 1) * scale : 0;
}
